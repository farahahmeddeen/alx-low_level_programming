#include "3-calc.h"
/**
 * op_add - function that adds interger
 * @a: first interger
 * @b: second interger
 * Return: addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that subtracts
 * @a: first interger
 * @b: second interger
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that multiples
 * @a: first interger
 * @b: second interger
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that divides
 * @a: first interger
 * @b: second interger
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that returns remainder
 * @a: first interger
 * @b: second interger
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
